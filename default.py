def fun(food):
    for c in food:
        print(c)

fruits=["dosa","idli"]
fun(fruits)

# The function fun is called with food being ["dosa", "idli"].
# The for loop iterates over the elements of the food list:
# On the first iteration, c is "dosa", so it prints "dosa".
# On the second iteration, c is "idli", so it prints "idli".